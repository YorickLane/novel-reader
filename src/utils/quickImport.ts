陈宇深吸一口气。空气中飘荡着复杂而熟悉的味道：刚收割的稻草散发着特有的清香，混合着远处鱼塘传来的水腥味，还有各家各户飘出的柴火饭香。偶尔一阵风过，会带来栀子花的幽香，或是某户人家养的茉莉花的清甜。

这些味道让他恍惚，让他觉得这十年好像只是一场梦。

终于，他站在了梁家门口。

